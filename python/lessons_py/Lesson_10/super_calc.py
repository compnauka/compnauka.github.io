# Практична робота

length = int(input("Довжина "))
width = int(input("Ширина "))
answer = input('''Доброго дня!
Якщо ви хочете розрахувати площу - натисніть 1 ,
якщо бажаєте розрахувати периметер - натисніть 2 ''')
if answer == "1":
    print("Площа дорівнює ", length * width)
elif answer == "2":
    print("Периметер дорівнює ", (length + width)*2)
else:
    print("Ви щось зробили не так")