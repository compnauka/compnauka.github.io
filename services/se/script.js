// –î–∏–Ω–∞–º—ñ—á–Ω–µ –ø–æ–∑–∏—Ü—ñ–æ–Ω—É–≤–∞–Ω–Ω—è tooltip
function showTooltip(icon, tooltip) {
  // –°–ø–æ—á–∞—Ç–∫—É —Ä–æ–±–∏–º–æ tooltip –≤–∏–¥–∏–º–∏–º, –∞–ª–µ –Ω–µ –Ω–∞ –µ–∫—Ä–∞–Ω—ñ
  tooltip.style.cssText = `
        display: block !important;
        top: -9999px !important;
        left: -9999px !important;
        z-index: 9999 !important;
        visibility: visible !important;
        opacity: 1 !important;
    `;

  // –û—Ç—Ä–∏–º—É—î–º–æ —Ä–æ–∑–º—ñ—Ä–∏ —ñ–∫–æ–Ω–∫–∏ —Ç–∞ tooltip
  const iconRect = icon.getBoundingClientRect();
  const tooltipRect = tooltip.getBoundingClientRect();

  // –û—Ç—Ä–∏–º—É—î–º–æ —Ä–æ–∑–º—ñ—Ä–∏ –≤—ñ–∫–Ω–∞ –±—Ä–∞—É–∑–µ—Ä–∞
  const windowWidth = window.innerWidth;
  const windowHeight = window.innerHeight;

  // –†–æ–∑—Ä–∞—Ö–æ–≤—É—î–º–æ –¥–æ—Å—Ç—É–ø–Ω–∏–π –ø—Ä–æ—Å—Ç—ñ—Ä –∑ –∫–æ–∂–Ω–æ–≥–æ –±–æ–∫—É —ñ–∫–æ–Ω–∫–∏
  const spaceAbove = iconRect.top;
  const spaceBelow = windowHeight - iconRect.bottom;
  const spaceLeft = iconRect.left;
  const spaceRight = windowWidth - iconRect.right;

  // –†–æ–∑—Ä–∞—Ö–æ–≤—É—î–º–æ –ø–æ–∑–∏—Ü—ñ—é –¥–ª—è –∫–æ–∂–Ω–æ–≥–æ –≤–∞—Ä—ñ–∞–Ω—Ç—É —Ä–æ–∑–º—ñ—â–µ–Ω–Ω—è
  let position, top, left, transform;

  // 1. –°–ø—Ä–æ–±—É—î–º–æ —Ä–æ–∑–º—ñ—Å—Ç–∏—Ç–∏ –≤–Ω–∏–∑—É (–ø–µ—Ä—à–∏–π –ø—Ä—ñ–æ—Ä–∏—Ç–µ—Ç)
  if (spaceBelow >= tooltipRect.height + 10 || spaceBelow >= spaceAbove) {
    position = "bottom";
    top = iconRect.bottom + 10;
    left = iconRect.left + iconRect.width / 2;
    transform = "translateX(-50%)";
  }
  // 2. –°–ø—Ä–æ–±—É—î–º–æ —Ä–æ–∑–º—ñ—Å—Ç–∏—Ç–∏ –≤–≥–æ—Ä—ñ
  else if (spaceAbove >= tooltipRect.height + 10) {
    position = "top";
    top = iconRect.top - tooltipRect.height - 10;
    left = iconRect.left + iconRect.width / 2;
    transform = "translateX(-50%)";
  }
  // 3. –°–ø—Ä–æ–±—É—î–º–æ —Ä–æ–∑–º—ñ—Å—Ç–∏—Ç–∏ —Å–ø—Ä–∞–≤–∞
  else if (spaceRight >= tooltipRect.width + 10) {
    position = "right";
    top = iconRect.top + iconRect.height / 2;
    left = iconRect.right + 10;
    transform = "translateY(-50%)";
  }
  // 4. –°–ø—Ä–æ–±—É—î–º–æ —Ä–æ–∑–º—ñ—Å—Ç–∏—Ç–∏ –∑–ª—ñ–≤–∞
  else if (spaceLeft >= tooltipRect.width + 10) {
    position = "left";
    top = iconRect.top + iconRect.height / 2;
    left = iconRect.left - tooltipRect.width - 10;
    transform = "translateY(-50%)";
  }
  // 5. –ó–∞–ø–∞—Å–Ω–∏–π –≤–∞—Ä—ñ–∞–Ω—Ç - —Ä–æ–∑–º—ñ—â—É—î–º–æ –Ω–∞–π–∫—Ä–∞—â–µ —è–∫ –º–æ–∂–µ–º–æ
  else {
    position = "center";
    // –í—Å—Ç–∞–Ω–æ–≤–ª—é—î–º–æ –ø–æ —Ü–µ–Ω—Ç—Ä—É –µ–∫—Ä–∞–Ω—É
    top = (windowHeight - tooltipRect.height) / 2;
    left = (windowWidth - tooltipRect.width) / 2;
    transform = "translate(0, 0)";
  }

  // –ü–µ—Ä–µ–≤—ñ—Ä—è—î–º–æ —á–∏ tooltip –Ω–µ –≤–∏—Ö–æ–¥–∏—Ç—å –∑–∞ –º–µ–∂—ñ –µ–∫—Ä–∞–Ω—É —Ç–∞ –∫–æ—Ä–∏–≥—É—î–º–æ —è–∫—â–æ –ø–æ—Ç—Ä—ñ–±–Ω–æ
  if (position === "bottom" || position === "top") {
    // –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞ —á–∏ –Ω–µ –≤–∏—Ö–æ–¥–∏—Ç—å –∑–∞ –ø—Ä–∞–≤–∏–π –∫—Ä–∞–π
    if (left + tooltipRect.width / 2 > windowWidth - 10) {
      left = windowWidth - tooltipRect.width - 10;
      transform = "translate(0, 0)";
    }
    // –ü–µ—Ä–µ–≤—ñ—Ä–∫–∞ —á–∏ –Ω–µ –≤–∏—Ö–æ–¥–∏—Ç—å –∑–∞ –ª—ñ–≤–∏–π –∫—Ä–∞–π
    if (left - tooltipRect.width / 2 < 10) {
      left = 10;
      transform = "translate(0, 0)";
    }
  }

  // –ó–∞—Å—Ç–æ—Å–æ–≤—É—î–º–æ —Ñ—ñ–Ω–∞–ª—å–Ω—ñ —Å—Ç–∏–ª—ñ
  tooltip.style.cssText = `
        display: block !important;
        top: ${top}px !important;
        left: ${left}px !important;
        transform: ${transform} !important;
        z-index: 9999 !important;
        visibility: visible !important;
        opacity: 1 !important;
    `;

  console.log(`üåü –¢–æ–ª—Ç—ñ–ø —Ä–æ–∑–º—ñ—â–µ–Ω–æ: ${position}`, { top, left, transform });
}

