function getConceptRootFromKey(conceptKey) {
    return String(conceptKey || "").trim().toLowerCase().split("-")[0];
}

function getSkill(conceptKey) {
    const root = getConceptRootFromKey(conceptKey);
    const skillMap = {
        alhorytmy: "algorithmic-thinking",
        dekompozytsiia: "decomposition",
        abstrahuvannia: "abstraction",
        rozpiznavannia: "pattern-recognition",
        povtorennia: "iteration",
        lohichne: "logic",
        efektyvnist: "efficiency",
        klasyfikatsiia: "classification",
        nalahodzhennia: "debugging",
        informatsiia: "information-handling",
        dani: "data-handling",
        testuvannia: "testing",
        modulnist: "modularity",
        otsiniuvannia: "evaluation",
        komandna: "collaboration",
        uzahalnennia: "generalization",
        refleksiia: "reflection"
    };

    return skillMap[root] || "computational-thinking";
}

function getDifficultyForQuestion(id, grade) {
    if (FOUNDATIONAL_DIFFICULTY_IDS.has(id)) {
        return "foundational";
    }

    if (ADVANCED_DIFFICULTY_IDS.has(id)) {
        return "advanced";
    }

    if (grade === "4") {
        return "developing";
    }

    return "developing";
}

function getProgressionBandForQuestion(id, grade) {
    if (RECOGNIZE_BAND_IDS.has(id)) {
        return "recognize";
    }

    if (REASON_BAND_IDS.has(id)) {
        return "reason";
    }

    if (grade === "4") {
        return "apply";
    }

    return "apply";
}

function enrichQuestion(question, grade, index) {
    const id = `${grade}-${index + 1}`;
    const conceptRoot = getConceptRootFromKey(question.conceptKey);
    const isExtension = EXTENSION_CONCEPT_KEYS.has(question.conceptKey);
    const requiredRoots = REQUIRED_ROOTS_BY_GRADE[grade] || new Set();

    return {
        ...question,
        id,
        skill: getSkill(question.conceptKey),
        difficulty: getDifficultyForQuestion(id, grade),
        progressionBand: getProgressionBandForQuestion(id, grade),
        isCore: !isExtension && requiredRoots.has(conceptRoot),
        reviewStatus: REVISED_QUESTION_IDS.has(id)
            ? "revised"
            : isExtension
                ? "extension"
                : "approved",
        distractorQuality: STRONG_DISTRACTOR_IDS.has(id) ? "strong" : "medium"
    };
}

