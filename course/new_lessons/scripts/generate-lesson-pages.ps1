  $html = $html.Replace("{{H1_TITLE}}", $lesson.H1)
  $html = $html.Replace("{{META_DESCRIPTION}}", $lesson.Description)
  $html = $html.Replace("{{OG_TITLE}}", $lesson.PageTitle)
